#include "push_swap.h"

t_list	*ft_lstnew(int content)
{
	t_list	*new;

	new = (t_list *)malloc(sizeof(t_list));
	if (new == NULL)
		return (NULL);
	new->content = content;
	new->next = NULL;
	return (new);
}

int	ft_lstsize(t_list *lst)
{
	int		i;

	i = 0;
	while (lst)
	{
		i++;
		lst = lst->next;
	}
	return (i);
}

int	ft_lstcontent(t_list *lst, int index)
{
	int i;
	
	i = 0;
	while (lst != NULL && i < index - 1)
	{
		lst = lst->next;
		i++;
	}
	return (lst->content);
}
